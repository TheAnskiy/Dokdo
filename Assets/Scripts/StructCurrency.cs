public struct StructCurrency
{
    public static uint Gold;
    public static uint Silver;
    public static uint Coal;
    public static uint Iron;
    public static uint Wood;
    public static uint Fish;

    public StructCurrency(uint gold, uint silver, uint coal, uint iron, uint wood, uint fish)
    {
        Gold = gold;
        Silver = silver;
        Coal = coal;
        Iron = iron;
        Wood = wood;
        Fish = fish;
    }
}
